package org.example;

public class Squ {
    private int a;
    private int b;

    public Squ(int a, int b) {
        this.a = a;
        this.b = b;
    }
    public Squ(){this.a = 0; this.b = 0;}

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
